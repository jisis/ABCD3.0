package cu.uci.abcd.dataprovider.jisis.search.option;

/**
 * 
 * @author
 * @version
 * 
 */
public class Option {

	String field;
	String term;

	public Option(String field, String term) {
		super();
		this.field = field;
		this.term = term;
	}

	public String getField() {
		return field;
	}

	public void setField(String field) {
		this.field = field;
	}

	public String getTerm() {
		return term;
	}

	public void setTerm(String term) {
		this.term = term;
	}

}
