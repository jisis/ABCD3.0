package cu.uci.abos.widget.repeatable.field.util;

import cu.uci.abos.widget.repeatable.field.ControlType;

public class SubFieldDescription {
	
	private String[] texts;
	private int[] types;
	private ControlType[] controls;
	
	
	public SubFieldDescription(String[] texts, int[] types, ControlType[] controls){
		
		this.texts = texts;
		this.types = types;
		this.controls = controls;
	}
	
	//gets and sets
	
	public String[] getTexts(){return texts;}
	public void setTexts(String[] texts){this.texts = texts;}
	
	public int[] getTypes(){return types;}
	public void setTypes(int[] types){this.types = types;}
	
	public ControlType[] getControls(){return controls;}
	public void setControls(ControlType[] controls){this.controls = controls;}
	
	public void setDescriptions(String[] texts, int[] types, ControlType[] controls){
		this.texts = texts;
		this.types = types;
		this.controls = controls;
	}

}
